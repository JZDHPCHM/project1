package gbicc.irs.fbinterface.jpa.vo;

public class AddressInfo {

	public String fbDate;//通信地址
	public String communicationAddress;//通信地址
	public String contactPhoNumber;//联系电话
	public String address;//地址
	
	
	public String getFbDate() {
		return fbDate;
	}
	public void setFbDate(String fbDate) {
		this.fbDate = fbDate;
	}
	public String getCommunicationAddress() {
		return communicationAddress;
	}
	public void setCommunicationAddress(String communicationAddress) {
		this.communicationAddress = communicationAddress;
	}
	public String getContactPhoNumber() {
		return contactPhoNumber;
	}
	public void setContactPhoNumber(String contactPhoNumber) {
		this.contactPhoNumber = contactPhoNumber;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
}
