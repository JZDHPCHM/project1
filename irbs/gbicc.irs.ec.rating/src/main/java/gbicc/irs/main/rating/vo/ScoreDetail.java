package gbicc.irs.main.rating.vo;

public class ScoreDetail {

	public String indexType;
	public String inOutType;
	public String indexParentName;
	public String indexCode;
	public String indexName;
	public String indexOption;
	public String indexValue;
	public String oldValue;
	public String getIndexType() {
		return indexType;
	}
	public void setIndexType(String indexType) {
		this.indexType = indexType;
	}
	public String getInOutType() {
		return inOutType;
	}
	public void setInOutType(String inOutType) {
		this.inOutType = inOutType;
	}
	public String getIndexParentName() {
		return indexParentName;
	}
	public void setIndexParentName(String indexParentName) {
		this.indexParentName = indexParentName;
	}
	public String getIndexCode() {
		return indexCode;
	}
	public void setIndexCode(String indexCode) {
		this.indexCode = indexCode;
	}
	public String getIndexName() {
		return indexName;
	}
	public void setIndexName(String indexName) {
		this.indexName = indexName;
	}
	public String getIndexValue() {
		return indexValue;
	}
	public void setIndexValue(String indexValue) {
		this.indexValue = indexValue;
	}
	public String getOldValue() {
		return oldValue;
	}
	public void setOldValue(String oldValue) {
		this.oldValue = oldValue;
	}
	public String getIndexOption() {
		return indexOption;
	}
	public void setIndexOption(String indexOption) {
		this.indexOption = indexOption;
	}
	
	
}
